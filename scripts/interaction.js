const myPrivateKey = process.env.PRIVATE_KEY;
const myAddress = "0x29b9b412Cbdb7a18bF8E8dc1b63Ad5f29E94bCB5";
const receiverAddress = "0xC08eDDcb81dd4FCd44173AaA3ebEaBF928366F95";

async function interact() {
	// Create a web3 instance - Truyền vào địa chỉ của node để biết đang ở mạng nào.
	const web3 = await new Web3("https://eth-sepolia.g.alchemy.com/v2/taoEkEMuZAz0W_6_DnFX2h9BqkWtcMNe");
	const floppyContract = await new web3.eth.Contract(floopyAbi, floppyAddress);

	// Call functions from contract: Có 2 kiểu tương tác - call, send
	// call
	const myBalance = await floppyContract.methods.balanceOf(myAddress).call();
	console.log("My balance: ", myBalance);

	// send -> modify the state of blockchain.
	await web3.eth.accounts.wallet.add(myPrivateKey);
	const receiverBalanceBefore = await floppyContract.methods.balanceOf(receiverAddress).call();
	console.log("Receiver balance before: ", receiverBalanceBefore);

	const tx = await floppyContract.methods.transfer(receiverAddress, 100000).send({
		from: myAddress,
		// Maxium gas chấp nhấp trả cho 1 giao dịch.
		gas: 3000000
	});
	
	const receiverBalanceAfter = await floppyContract.methods.balanceOf(receiverAddress).call();
	console.log("Receiver balance after: ", receiverBalanceAfter);
	console.log(tx, receiverBalanceBefore, receiverBalanceAfter);
}
interact();